	public void displayModule(ErlangModule mod) {
		module = mod;
		moduleName.setText(mod.name);
		this.setTitle(mod.name);
		JLabel ta = new JLabel("<html>");
		for (List<OtpErlangObject> argList : mod.behaviour.getInitArgs()) {
			if (!ta.getText().equals("<html>")) {
				ta.setText(ta.getText() + "<br />");
			}
			StringBuffer argsAsString = new StringBuffer(ta.getText());
			argsAsString.append(' ');
			boolean first = true;
			for (OtpErlangObject o : argList) {
				if (!first)
					argsAsString.append(',');
				else
					first = false;
				argsAsString.append(o);
			}
			ta.setText(argsAsString.toString());
		}
		ta.setText(ta.getText() + "</html>");
		initVals.getViewport().removeAll();
		initVals.getViewport().add(ta, BorderLayout.CENTER);
		behaviour.setText(mod.behaviour.toString());
		ta = new JLabel("<html>");
		for (ErlangLabel a : mod.behaviour.getAlphabet()) {
			if (!ta.getText().equals("<html>")) {
				ta.setText(ta.getText() + "<br />");
			}
			ta.setText(ta.getText() + a.toString());
		}
		ta.setText(ta.getText() + "</html>");
		alphabet.getViewport().removeAll();
		alphabet.getViewport().add(ta, BorderLayout.CENTER);
		ta = new JLabel("<html>");
		for (String a : mod.behaviour.getDependencies()) {
			if (!ta.getText().equals("<html>")) {
				ta.setText(ta.getText() + "<br />");
			}
			ta.setText(ta.getText() + a);
		}
		ta.setText(ta.getText() + "</html>");
		dependencies.getViewport().removeAll();
		dependencies.getViewport().add(ta, BorderLayout.CENTER);
	}

